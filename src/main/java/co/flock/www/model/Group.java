public class Group {
    private String id;
    private String name;
    private int memberCount;
    private String profileImage;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getMemberCount() {
        return memberCount;
    }

    public String getProfileImage() {
        return profileImage;
    }

}
